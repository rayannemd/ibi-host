-- Remover tabelas se já existirem (ordem que evita conflito de FK)
DROP TABLE IF EXISTS checkout;
DROP TABLE IF EXISTS checkin;
DROP TABLE IF EXISTS reserva;
DROP TABLE IF EXISTS quarto;
DROP TABLE IF EXISTS cliente;
DROP TABLE IF EXISTS usuario;

-- Cliente (PK = CPF)
CREATE TABLE cliente (
    cpf       varchar(255) NOT NULL,
    nome      varchar(255),
    endereco  varchar(255),
    telefone  varchar(255),
    email     varchar(255),
    PRIMARY KEY (cpf)
);

-- Usuario (login)
CREATE TABLE usuario (
    id    bigint GENERATED BY DEFAULT AS IDENTITY,
    email varchar(255),
    senha varchar(255),
    PRIMARY KEY (id)
);

-- Quarto (PK = numero, atribuído manualmente — sem identity)
CREATE TABLE quarto (
    numero    integer NOT NULL,
    tipo      varchar(255),
    status    varchar(255),
    descricao varchar(255),
    PRIMARY KEY (numero)
);

-- Reserva
CREATE TABLE reserva (
    id                bigint GENERATED BY DEFAULT AS IDENTITY,
    data_checkin      date,
    data_checkout     date,
    valor_reserva     real,
    qtd_pessoas       integer,
    cpf_cliente       varchar(255) NOT NULL,
    numero_quarto     integer NOT NULL,
    checkin_realizado boolean DEFAULT false,
    PRIMARY KEY (id),
    CONSTRAINT fk_reserva_cliente FOREIGN KEY (cpf_cliente) REFERENCES cliente (cpf),
    CONSTRAINT fk_reserva_quarto  FOREIGN KEY (numero_quarto) REFERENCES quarto (numero)
);

-- Checkin
CREATE TABLE checkin (
    id                 bigint GENERATED BY DEFAULT AS IDENTITY,
    data_checkin       date,
    valor_checkin      real,
    id_reserva         bigint,
    checkout_realizado boolean DEFAULT false,
    PRIMARY KEY (id),
    CONSTRAINT fk_checkin_reserva FOREIGN KEY (id_reserva) REFERENCES reserva (id)
);

-- Checkout
CREATE TABLE checkout (
    id           bigint GENERATED BY DEFAULT AS IDENTITY,
    nome_cliente varchar(255),
    data_checkout date,
    id_checkin   bigint NOT NULL,
    valor_total  double precision,
    PRIMARY KEY (id),
    CONSTRAINT fk_checkout_checkin FOREIGN KEY (id_checkin) REFERENCES checkin (id)
);

